export const generateTemperatureHistory = (showcaseId: string) => {
  const data = [];
  const baseTemp = showcaseId === 's1' ? 27.5 : 23;
  for (let i = 23; i >= 0; i--) {
    const time = new Date(now.getTime() - i * 60 * 60 * 1000);
    const variation = (Math.random() - 0.5) * 3;
    data.push({
      time: `${time.getHours()}:00`,
      temperature: Math.round((baseTemp + variation) * 10) / 10,
      humidity: Math.round((50 + (Math.random() - 0.3) * 20) * 10) / 10,
    });
  }
  return data;
};

